#Python String Methods

palavra = 'hoje esTá Quente em Brasília, Fazendo 9654 mil GrAus de TemPeRaTuRa'
print("String: ",palavra)

print("casefold")
print(palavra.casefold())

print("capitalize")
print(palavra.capitalize())

print("count")
print(palavra.count('e'))

print("upper")
print(palavra.upper())

print("lower")
print(palavra.lower())

print("swapcase")
print(palavra.swapcase())

print("replace")
print(palavra.replace('Quente', 'embaixo da mesa'))

print("split")
print(palavra.split(','))
